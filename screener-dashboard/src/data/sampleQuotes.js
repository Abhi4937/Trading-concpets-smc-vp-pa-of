/**
 * sampleQuotes.js — Static sample data for NSE F&O universe
 *
 * Numbers are internally consistent with the classifier in screener.js:
 *   - breakout: ltp > max(orHigh,pdh) && ltp > vwap && relVol>=2 && changePct>0
 *   - breakdown: ltp < min(orLow,pdl) && ltp < vwap && relVol>=2 && changePct<0
 *   - fakeout: dayHigh > max(orHigh,pdh) but ltp < vwap (or mirror for lows)
 *   - reversal_up: changePct<0 but ltp > vwap && relVol>=1.5 && rsRank>=50
 *   - bounce: ltp > vwap && changePct>0 && |ltp-vwap|/vwap < 0.006 && relVol>=1.5
 *
 * dayHigh >= max(ltp, open, orHigh) always
 * dayLow  <= min(ltp, open, orLow) always
 */

function riseSpark(start, end, n = 30) {
  const pts = [];
  for (let i = 0; i < n; i++) {
    const t = i / (n - 1);
    const noise = (Math.sin(i * 2.3) * 0.003 + Math.cos(i * 1.7) * 0.002) * start;
    pts.push(+(start + (end - start) * t + noise).toFixed(2));
  }
  return pts;
}

function fallSpark(start, end, n = 30) {
  const pts = [];
  for (let i = 0; i < n; i++) {
    const t = i / (n - 1);
    const noise = (Math.sin(i * 2.1) * 0.003 + Math.cos(i * 1.9) * 0.002) * start;
    pts.push(+(start + (end - start) * t + noise).toFixed(2));
  }
  return pts;
}

function fakeoutSpark(start, spike, end, n = 30) {
  const pts = [];
  const peakAt = Math.floor(n * 0.55);
  for (let i = 0; i < n; i++) {
    let v;
    if (i <= peakAt) {
      const t = i / peakAt;
      v = start + (spike - start) * t;
    } else {
      const t = (i - peakAt) / (n - 1 - peakAt);
      v = spike + (end - spike) * t;
    }
    const noise = (Math.sin(i * 2.5) * 0.002) * start;
    pts.push(+(v + noise).toFixed(2));
  }
  return pts;
}

function flatWithVwap(vwap, n = 30) {
  const pts = [];
  for (let i = 0; i < n; i++) {
    const noise = (Math.sin(i * 3.1) * 0.002) * vwap;
    pts.push(+(vwap + noise).toFixed(2));
  }
  return pts;
}

